package topic_07_packages_and_math_api;

public class L01 {
    static void main(String[] args) {
        System.out.println(Math.min(10,12)); // gives minimum of 2 numbers
        System.out.println(Math.max(10,12)); // gives maximum of 2 numbers
        System.out.println(Math.abs(-10));//gives absolute +ve value
        System.out.println((int)Math.floor(5.8));//gives absolute +ve value
        System.out.println((int)Math.ceil(5.8));//gives absolute +ve value
        System.out.println((int)Math.round(15.4));//gives absolute +ve value
        System.out.println(Math.round(15.5));//gives absolute +ve value
        System.out.println(Math.sqrt(16));//gives absolute +ve value
        System.out.println(Math.cbrt(27));//gives absolute +ve value
        System.out.println(Math.pow(3,8));//gives absolute +ve value

    }
}
